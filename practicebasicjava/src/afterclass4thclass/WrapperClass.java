package afterclass4thclass;

public class WrapperClass {

    public static void main(String[] args) {

        String robin = "1000";
        System.out.println(robin);
        System.out.println(robin+20);
        int zaya = Integer.parseInt(robin);
        System.out.println(zaya+20);


        String tofayel = "10099";
        double shohan = Double.parseDouble(tofayel);
        System.out.println(shohan);


        String salma = "true";
        boolean farida = Boolean.parseBoolean(salma);
        System.out.println(farida);




    }
}
